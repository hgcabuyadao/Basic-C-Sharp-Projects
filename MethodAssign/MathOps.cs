using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MethodAssign
{
    class MathOps

    {
        public int Sum(int p, int q)
        {
            return (p + q);

        }

        public int Multiply(int p, int q)
        {
            return (p * q);

        }

        public int Subtract(int p, int q)
        {
            return (p - q);

        }
    }
}
        


